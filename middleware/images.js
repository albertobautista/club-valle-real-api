const debug = require('debug')('images');

/***********************************************************************************************/
/************************************* Module Implementation ***********************************/
/***********************************************************************************************/
function DeserializeImage(req, res, next) {
    let fileUri = '';
    if (req.file) {
        fileUri                         = process.env.APP_URL + ':' + process.env.SERVER_PORT + '/' + req.file.filename;
        req.body[req.file.fieldname]    = fileUri;
    }
    next();
}

/***********************************************************************************************/
/******************************************** Exports ******************************************/
/***********************************************************************************************/

module.exports = DeserializeImage;